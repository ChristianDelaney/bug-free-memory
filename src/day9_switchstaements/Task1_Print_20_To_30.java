package day9_switchstaements;

public class Task1_Print_20_To_30 {

	public static void main(String[] args) {
		
		//do while loop
		
		int a = 20;
		  do {
		        System.out.println(a);    
		        a++;
		                
		        } while (a<31);
		  
		 
		System.out.println("-----------------------");  
		  
		  
		  // for loop
		  
		  for (int i =20 ; i < 31; i++) {System.out.println(i);}
		  
		 
		System.out.println("-----------------------"); 
		  
	
		  // while loop
		  
		  int b = 20;
		  
		  while (b <= 30) {
			  System.out.println(b);
			  b++;
			  
		  }

	}

}
