package day11_review;

public class Loops {

	public static void main(String[] args) {
		
		// while loop
		
		char a = 'a';
		while (a <= 122) {
			System.out.println(a);
			a++;
		}

		
		// do while loop
		int i = 1;
		do {
			System.out.println("Hi "+i);
			i++;	
		} while (i <  10);
		
		//for loop
		
		for (int j = 0;j < 5; j++ ) {
			System.out.println("Hi "+j);
		}
		
		
		
		
	
		
		
		
		
	}

}
