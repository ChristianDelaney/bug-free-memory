public class StringConcatenation {

	public static void main(String[] args) {
		String make = "2022 Honda";
		String model = "Accord";
		
		System.out.println(make);
		System.out.println(model);
		
		System.out.println("next year my new car will be " +make+" "+model);
		
		String name = "Christian";
		
		String child = "1";
		
		System.out.println("my name is "+name+" and I have "+child+" child");
		
		
		

	}

}
